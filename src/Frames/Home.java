    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        DefaultTableModel model = (DefaultTableModel) tbl_Address.getModel();
        if (!txta_address.getText().isEmpty()) {

            Connection con = MysqlConnect.getConnection();
            PreparedStatement ps;
            ResultSet rs;
            if (!new DtbsHelper().isUsernameExist(lbl_usernameUp.getText(), "address")) {
                try {
                    ps = con.prepareStatement("INSERT INTO `address`(`username`,`address1`, `group1`) VALUES (?,?,?)");
                    ps.setString(1, lbl_usernameUp.getText());
                    ps.setString(2, txta_address.getText());
                    ps.setString(3, chbx_group.getSelectedItem().toString());
                    if (ps.executeUpdate() != 0) {
                        JOptionPane.showMessageDialog(null, "Your address Succesfully Added");
                        model.addRow(new Object[]{txta_address.getText(), chbx_group.getSelectedItem().toString()});

                    } else {

                        JOptionPane.showMessageDialog(null, "Something went wrong:(");

                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else {
                try {
                    ps = con.prepareStatement("SELECT `address1`,`address2`,`address3` FROM `address` WHERE `username`=?");
                    ps.setString(1, lbl_usernameUp.getText());
                    rs = ps.executeQuery();
                    if (rs.next()) {
                        String a1 = rs.getString(1);
                        String a2 = rs.getString(2);
                        String a3 = rs.getString(3);
                        if (a1 == null) {
                            String query = "UPDATE `address`  SET address1='" + txta_address.getText() + "', group1='" + chbx_group.getSelectedItem().toString() + "'where username='" + lbl_usernameUp.getText() + "'";
                            DtbsHelper.edıtInfo(6, query);
                            model.addRow(new Object[]{txta_address.getText(), chbx_group.getSelectedItem().toString()});

                        } else if (a2 == null) {
                            String query = "UPDATE `address`  SET address2='" + txta_address.getText() + "', group2='" + chbx_group.getSelectedItem().toString() + "'where username='" + lbl_usernameUp.getText() + "'";
                            DtbsHelper.edıtInfo(6, query);
                            model.addRow(new Object[]{txta_address.getText(), chbx_group.getSelectedItem().toString()});

                        } else if (a3 == null) {
                            String query = "UPDATE `address`  SET address3='" + txta_address.getText() + "', group3='" + chbx_group.getSelectedItem().toString() + "'where username='" + lbl_usernameUp.getText() + "'";
                            DtbsHelper.edıtInfo(6, query);
                            model.addRow(new Object[]{txta_address.getText(), chbx_group.getSelectedItem().toString()});

                        } else {
                            JOptionPane.showMessageDialog(null, "You can only add 3!");
                        }

                    } else {
                        JOptionPane.showMessageDialog(null, "Login Error");
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Something went Wrong");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please fill this area before adding!");

        }


    }//GEN-LAST:event_btn_addActionPerformed

