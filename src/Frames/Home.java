    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        if (tbl_Address.getSelectedRow() != -1) {
            // remove selected row from the model
            Connection con = MysqlConnect.getConnection();
            PreparedStatement ps;
            ResultSet rs;
            try {
                ps = con.prepareStatement("SELECT `address1`,`address2`,`address3` FROM `address` WHERE `username`=?");
                ps.setString(1, lbl_usernameUp.getText());
                rs = ps.executeQuery();
                if (rs.next()) {
                    String a1 = rs.getString(1);
                    String a2 = rs.getString(2);
                    String a3 = rs.getString(3);
                    if (tbl_Address.getValueAt(tbl_Address.getSelectedRow(), 0).equals(a1)) {
                        String query = "UPDATE `address`  SET address1=NULL , group1=NULL WHERE username='" + lbl_usernameUp.getText() + "'";
                        DtbsHelper.edıtInfo(7, query);
                    } else if (tbl_Address.getValueAt(tbl_Address.getSelectedRow(), 0).equals(a2)) {
                        String query = "UPDATE `address`  SET address2=NULL, group2=NULL where username='" + lbl_usernameUp.getText() + "'";
                        DtbsHelper.edıtInfo(7, query);
                    } else if (tbl_Address.getValueAt(tbl_Address.getSelectedRow(), 0).equals(a3)) {
                        String query = "UPDATE `address`  SET address3=NULL , group3=NULL where username='" + lbl_usernameUp.getText() + "'";
                        DtbsHelper.edıtInfo(7, query);

                    }
                }
            } catch (SQLException ex) {
                Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
            }
            DefaultTableModel model = (DefaultTableModel) tbl_Address.getModel();
            model.removeRow(tbl_Address.getSelectedRow());
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

